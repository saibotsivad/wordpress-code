<h1>Template Foundation</h1>
<p>This is not really a theme, my apologies if you thought it was.</p>
<p>Inside the theme folder is a set of HTML layouts, structured in different ways and prepared with a Just Right amount of Wordpress template tags.</p>
<p>These templates are for <strong>theme designers</strong>, who want to stop messing around with HTML structure and get straight to the CSS styling.</p>
<h2>How to Use:</h2>
<ol>
	<li>Find the HTML file in the "templates" folder that best matches your basic layout.</li>
	<li>Copy + past the files into your new theme folder.</li>
	<li>Modify the template to display what you want.</li>
	<li>Modify the CSS to get things looking "just so".</li>
	<li>...</li>
	<li>Profit!</li>
</ol>
<p>The templates are split into custom files wherever I thought it would make design easier. It is easy enough to composite them back into single/less files, but there is no need to do so.</p>
<p>The addition of Wordpress tags, and manipulation of HTML to fit Wordpress norms was done by me, <a href="http://tobiaslabs.com">Tobias</a>, please contact me if you think of another layout that would be helpful, or other elements which could be added, or if you notice errors.</p>
<h2>Important Notes:</h2>
<p>If you enable your template before changing the name in the <code>style.css</code> file, you may break your Wordpress. Please change the name and <em>then</em> activate it.</p>
<p>The <code>style.css</code> of all the templates are the same, so don't copy them all to the <code>themes</code> folder at the same time.</p>
